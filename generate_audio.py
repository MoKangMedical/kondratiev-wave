async def generate_audio(num, text):
    """Generate audio for a single course."""
    output_file = f"{OUTPUT_DIR}/lesson{num}.mp3"
    communicate = edge_tts.Communicate(text, VOICE, rate="+0%")
    await communicate.save(output_file)
    print(f"Generated: lesson{num}.mp3")

async def main():
    for num, text in audio_content.items():
        await generate_audio(num, text)
    print("All audio files generated!")

if __name__ == "__main__":
    asyncio.run(main())
